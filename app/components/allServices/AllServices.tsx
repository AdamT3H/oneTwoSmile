export default function AllServices() {
  const [selected, setSelected] = useState("Усі");
  const [expandedServices, setExpandedServices] = useState<
    Record<number, boolean>
  >({});

  const toggleExpand = (id: number) => {
    setExpandedServices((prev) => ({ ...prev, [id]: !prev[id] }));
  };

  const filteredServices =
    selected === "Усі"
      ? services
      : services.filter((service) => service.category === selected);

  return (
    <div className={styles.container}>
      <div className={styles.options}>
        {[
          "Усі",
          "Догляд за зубами",
          "Лікування зубів",
          "Косметологія",
          "Ше шось",
          "Усі2",
          "Догляд за зубами2",
          "Лікування зубів2",
          "Косметологія2",
          "Ше шось2",
        ].map((option) => (
          <div
            key={option}
            className={`${styles.option} ${
              selected === option ? styles.active : ""
            }`}
            onClick={() => setSelected(option)}
          >
            {option}
          </div>
        ))}
      </div>

      <div className={styles.servicesList}>
        {filteredServices.length > 0 ? (
          filteredServices.map((service) => (
            <div key={service.id} className={styles.serviceCard}>
              <h3 className={styles.serviceName}>{service.name}</h3>
              <div className={styles.details}>
                <span className={styles.duration}>
                  Тривалість: {service.duration}
                </span>
                <span className={styles.price}>Ціна: {service.price}</span>
              </div>
              <p>
                {expandedServices[service.id]
                  ? service.description
                  : `${service.description.slice(0, 150)}...`}
              </p>
              <button
                className={styles.toggleButton}
                onClick={() => toggleExpand(service.id)}
              >
                {expandedServices[service.id] ? "Згорнути" : "Читати більше"}
              </button>
            </div>
          ))
        ) : (
          <p>Послуг у цій категорії немає.</p>
        )}
      </div>
    </div>
  );
}

